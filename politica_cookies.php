        <p>são cookies essenciais que possibilitam a navegação em nossas aplicações e o acesso a todos os recursos; sem estes, nossos serviços podem apresentar mal desempenho ou não funcionar.</p>
        <h4>Cookies de desempenho:</h4>
        <p>são cookies que otimizam a forma que nossas aplicações funcionam, coletando informações anônimas sobre as páginas acessadas.</p>
        <h4>Cookies de funcionalidade:</h4>
        <p>são cookies que memorizam suas preferências e escolhas (como seu nome de usuário)</p>
        <h4>Cookies de publicidade:</h4>
        <p>são cookies que direcionam anúncios em função dos seus interesses e limitam a quantidade de vezes que o anúncio aparece.</p>
