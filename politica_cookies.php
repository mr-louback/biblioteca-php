        <h4>Cookies de terceiros:</h4>
        <p>são cookies definidos por terceiros confiáveis em nossa aplicação</p>
        <p>Os cookies, quanto ao seu tempo de vida, podem ser:</p>
        <h4>Cookies de sessão ou temporários:</h4>
        <p>são cookies que expiram assim que você fecha o seu navegador, encerrando a sessão.</p>
        <h4>Cookies persistentes ou permanentes:</h4>
        <p>são cookies que permanecem no seu dispositivo durante um período determinado ou até que você os exclua.</p>
        <p>Os cookies, quanto a sua finalidade, podem ser:</p>
